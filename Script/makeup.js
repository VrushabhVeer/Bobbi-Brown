function sortItems() {
    var sorting = document.querySelector("#sorter").value;

    if (sorting == "H2L") {
        makeup.sort(function (a, b) {
            return b.price - a.price;
        })
        displayData(makeup);

    }
    else if (sorting === "L2H") {
        makeup.sort(function (a, b) {
            return a.price - b.price;
        })
        displayData(makeup);
    }
}
