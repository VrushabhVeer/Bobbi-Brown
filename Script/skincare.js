function sortItems() {
    var sorting = document.querySelector("#sorter").value;

    if (sorting == "H2L") {
        skincareProduct.sort(function (a, b) {
            return b.price - a.price;
        })
        displayData(skincareProduct);

    }
    else if (sorting === "L2H") {
        skincareProduct.sort(function (a, b) {
            return a.price - b.price;
        })
        displayData(skincareProduct);
    }
}
